// if statement
const userLoggedIn = true;

if (userLoggedIn) {
  console.log("Welcome back!");
}

// Interesting fact: The `if` statement allows you to execute code conditionally based on whether the expression evaluates to true or false.

// if-else statement
const userAge = 18;

if (userAge >= 18) {
  console.log("You are eligible to vote.");
} else {
  console.log("You are not eligible to vote.");
}

// Interesting fact: The `if-else` statement allows you to execute one block of code if the condition is true and another block if it's false.
